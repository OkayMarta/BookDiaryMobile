package com.example.bookdiarymobile.data

/**
 * Перелічення (Enum), що визначає можливі статуси книги в щоденнику.
 *
 * Використовується для фільтрації книг у списках та визначення логіки
 * відображення та обробки даних.
 */
enum class BookStatus {
    READ, // Статус для книги, яка вже була прочитана.
    TO_READ // Статус для книги, яку користувач планує прочитати в майбутньому.
}